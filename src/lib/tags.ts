/**
 * タグ名をURLパスセグメントとして安全な「スラグ」に変換する。
 *
 * `+` のような記号はURL/クエリ文字列のデコード処理の過程でスペースとして
 * 誤解釈されたり、ルーティングの正規表現マッチングで特殊文字として扱われたりして
 * 404の原因になりやすい（例: "C++" タグ）。該当する記号だけを安全な文字に置き換える。
 * 日本語などのUnicode文字はAstroが内部で適切にエンコード/デコードするため、そのまま使う。
 */
export function tagToSlug(tag: string): string {
  return tag
    .replace(/\+/g, '-plus')
    .replace(/[#?/%&]/g, '-');
}
